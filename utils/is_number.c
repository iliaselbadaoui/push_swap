#include "utils.h"

t_bool	is_number(t_string nbr)
{
	if (*nbr == '-' || *nbr == '+')
		nbr++;
	while (*nbr)
	{
		if ((*nbr > '9' || *nbr < '0'))
			return (false);
		nbr++;
	}
	return (true);
}
